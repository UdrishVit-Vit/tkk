function cleanTitle(text) {
  return cleanMarkdown(text)
    .replace(/\s*\([^)]*архетип[^)]*\)\s*/ig, ' ')
    .replace(/\s+/g, ' ')
    .trim()
}

function cleanFeatureTitle(title, fallback) {
  return cleanTitle(title)
    .replace(/^\s*\d+\s*[-‑–]?\s*(?:й|го)?\s*уровень\s*[—–-]?\s*/i, '')
    .replace(/\s*[*_]*\(?\s*\d+\s*[-‑–]?(?:й|го|уровень|ур\.)[^)]*\)?\s*[*_]*$/i, '')
    .replace(/^\d+\.\s*/, '')
    .replace(/[.:]\s*$/, '')
    .trim() || fallback
}

