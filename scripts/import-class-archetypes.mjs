function walk(dir) {
  return fs.readdirSync(dir, { withFileTypes: true }).flatMap(entry => {
    const full = path.join(dir, entry.name)
    return entry.isDirectory() ? walk(full) : [full]
  })
}

function stripFrontmatter(text) {
  return text.replace(/^---\r?\n[\s\S]*?\r?\n---\s*/, '')
}

function frontmatter(text) {
  const match = text.match(/^---\r?\n([\s\S]*?)\r?\n---/)
  if (!match) return {}
  const data = {}
  const lines = match[1].split(/\r?\n/)
  for (let i = 0; i < lines.length; i++) {
    const key = lines[i].match(/^([^:#]+):\s*(.*)$/)
    if (!key) continue
    if (key[1].trim() === 'authors') {
      const authors = []
      const inline = key[2].trim()
      if (inline && inline !== '[]') authors.push(inline.replace(/^[-\s]+/, '').trim())
      for (let j = i + 1; j < lines.length && /^\s*-\s+/.test(lines[j]); j++) {
        authors.push(lines[j].replace(/^\s*-\s+/, '').trim())
        i = j
      }
      data.authors = authors
    } else {
      data[key[1].trim()] = key[2].trim().replace(/^['"]|['"]$/g, '')
    }
  }
  return data
}

