function collectFiles() {
  return walk(root).filter(file => {
    if (!file.endsWith('.md')) return false
    const rel = path.relative(root, file)
    const normalizedRel = rel.replace(/\\/g, '/')
    if (skipFiles.has(normalizedRel)) return false
    const parts = rel.split(path.sep)
    if (parts.some(part => skipDirs.has(part))) return false
    const base = path.basename(file)
    if (/0\.33/.test(base)) return false
    if (/^MOC-|^Шаблон/i.test(base)) return false
    return true
  })
}

const imported = {}
const skipped = []

for (const file of collectFiles().sort((a, b) => a.localeCompare(b, 'ru'))) {
  const rel = path.relative(root, file)
  const dir = rel.split(path.sep)[0]
  const className = classMap[dir]
  if (!className) {
    skipped.push([rel, 'unknown class'])
    continue
  }

  const raw = fs.readFileSync(file, 'utf8')
  if (!raw.trim()) {
    skipped.push([rel, 'empty'])
    continue
  }

  const meta = frontmatter(raw)
  if (/0\.33/.test(String(meta.version || ''))) {
    skipped.push([rel, 'version 0.33'])
    continue
  }

  let body = stripFrontmatter(raw)
  const initialH1 = titleFromBody(body)
  body = cleanMarkdown(body, { keepHeadings: true })
  const normalizedRel = rel.replace(/\\/g, '/')
  const split = normalizedRel === 'Чародей/Чародей - Ткач Искры.md'
    ? splitSorcererWeaver(body) || splitSections(body)
    : normalizedRel === 'Воин/Воин - Клинок Духов.md'
      ? splitFighterSpiritBlade()
      : normalizedRel === 'Волшебник/Волшебник - Хакар.md'
        ? splitWizardHakar()
        : normalizedRel === 'Волшебник/Волшебник - Школа Воли.md'
          ? splitWizardWillSchool()
          : normalizedRel === 'Волшебник/Волшебник - Школа Клыков.md'
            ? splitWizardFangsSchool()
            : splitSections(body)
  const h1 = initialH1 || cleanFeatureTitle(split.sections[0]?.title || '', '')
  if (!h1 || /\{\{/.test(h1)) {
    skipped.push([rel, 'no real h1'])
    continue
  }
  const classOnlyName = path.basename(file, '.md').replace(/\s+\d+(?:\.\d+)*$/, '').trim()
  const looksLikeSubclassFile = /\s+-\s+/.test(classOnlyName)
  if (normalizeForName(classOnlyName) === normalizeForName(dir) || (normalizeForName(h1) === normalizeForName(className) && !looksLikeSubclassFile)) {
    skipped.push([rel, 'class page'])
    continue
  }

  const [source, sourceFullName] = sourceFromAuthors(meta.authors)
  const name = nameOverrides[normalizedRel] || nameFromH1(h1, className, split.sections, nameFromFile(file, dir))
  const defaultRank = baseLevel[className] || 3
  const seq = progression[className] || [defaultRank]
  const introText = introParagraphs(split.intro, normalizedRel === 'Чародей/Чародей - Ткач Искры.md' ? 1 : 2).join('\n\n')
  const features = split.sections.map((section, index) => {
    const rank = rankFrom(section.title) || rankFromOpening(section.text) || section.parentRank || seq[Math.min(index, seq.length - 1)] || defaultRank
    const feature = {
      name: featureNameFromSection(section, `${name}: умение ${index + 1}`),
      level: levelLabel(rank),
      rank,
      text: cleanMarkdown(section.text)
    }
    if (section.items?.length) {
      feature.items = section.items
      feature.itemsTitle = section.itemsTitle || ''
      feature.itemsCollapsed = !!section.itemsCollapsed
    }
    return feature
  }).filter(feature => feature.name && (feature.text || feature.name))
    .sort((a, b) => (a.rank || 999) - (b.rank || 999) || a.name.localeCompare(b.name, 'ru'))

  if (!features.length) {
    features.push({ name, level: levelLabel(defaultRank), rank: defaultRank, text: introText })
  }

  const archetype = {
    id: `${className.toLowerCase().replace(/\s+/g, '-')}-${simpleHash(rel)}`,
    name,
    shortName: name,
    source,
    sourceFullName,
    level: levelLabel(defaultRank),
    type: typeMap[className] || 'Подкласс',
    summary: firstParagraph(introText || features[0].text).slice(0, 360),
    intro: introText ? introText.split(/\n{2,}/).filter(Boolean) : [],
    features
  }

  ;(imported[className] ||= []).push(archetype)
}

for (const list of Object.values(imported)) {
  list.sort((a, b) => a.name.localeCompare(b.name, 'ru'))
}

