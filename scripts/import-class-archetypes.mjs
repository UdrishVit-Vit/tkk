function splitSorcererWeaver(body) {
  const levelMatches = [...body.matchAll(/^(?:###\s+)?Уровень\s+(\d+)\s*:\s*(.+)$/gm)]
  if (!levelMatches.length) return null
  const firstLevelIndex = levelMatches[0].index
  const intro = stripH1(body.slice(0, firstLevelIndex))
  const sections = levelMatches.map((match, index) => {
    const start = match.index + match[0].length
    const end = levelMatches[index + 1]?.index ?? body.search(/^#\s+ГНЕВ ИЛЬБЕША/im)
    const safeEnd = end >= 0 ? end : body.length
    return {
      title: `${match[2]} (${match[1]}-й уровень)`,
      text: body.slice(start, safeEnd).trim()
    }
  })
  return { intro, sections }
}

