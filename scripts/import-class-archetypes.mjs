function simpleHash(input) {
  let hash = 2166136261
  for (let i = 0; i < input.length; i++) {
    hash ^= input.charCodeAt(i)
    hash = Math.imul(hash, 16777619)
  }
  return (hash >>> 0).toString(36)
}

function normalizeForName(text) {
  return cleanTitle(text).toLowerCase().replace(/ё/g, 'е').replace(/[^a-zа-я0-9]+/gi, ' ').trim()
}

function nameFromFile(file, dir) {
  const base = path.basename(file, '.md').replace(/\s+\d+(?:\.\d+)*$/, '').trim()
  const parts = base.split(/\s+-\s+/)
  if (parts.length < 2) return ''
  return cleanTitle(parts.slice(1).join(' - ').replace(/\s+/g, ' '))
}

function nameFromH1(h1, className, sections, fileNameCandidate = '') {
  let name = cleanTitle(h1)
  for (const cls of [className, className.toUpperCase()]) {
    name = name.replace(new RegExp(`^${cls}\\s*[:—-]\\s*`, 'i'), '')
    name = name.replace(new RegExp(`^${cls}\\s+`, 'i'), '')
  }
  name = name.replace(/^Потусторонний покровитель:\s*/i, '')
  name = name.replace(/^Чародейское происхождение:\s*/i, '')
  name = name.replace(/\s*\([^)]*ЭНОА[^)]*\)\s*/i, '').trim()
  if (fileNameCandidate) {
    name = fileNameCandidate
  } else if (!name || normalizeForName(name) === normalizeForName(className)) {
    name = cleanFeatureTitle(sections[0]?.title || h1, h1)
  }
  return cleanTitle(name)
}

