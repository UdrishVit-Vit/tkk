function cleanTitle(text) {
  return cleanMarkdown(text)
    .replace(/\s*\([^)]*архетип[^)]*\)\s*/ig, ' ')
    .replace(/\s+/g, ' ')
    .trim()
}

function cleanFeatureTitle(title, fallback) {
  return cleanTitle(title)
    .replace(/^\s*\d+\s*[-‑–]?\s*(?:й|го)?\s*уровень\s*[—–-]?\s*/i, '')
    .replace(/\s*[*_]*\(?\s*\d+\s*[-‑–]?(?:й|го|уровень|ур\.)[^)]*\)?\s*[*_]*$/i, '')
    .replace(/^\d+\.\s*/, '')
    .replace(/[.:]\s*$/, '')
    .trim() || fallback
}

function titleFromBody(body) {
  const h1 = body.match(/^#\s+(.+)$/m)
  return h1 ? cleanTitle(h1[1]) : ''
}

function stripH1(body) {
  return body.replace(/^#\s+.+\r?\n?/, '').trim()
}

function splitSections(body) {
  const matches = [...body.matchAll(/^##\s+(.+)$/gm)]
  if (!matches.length) return { intro: stripH1(body), sections: [] }
  const intro = body.slice(0, matches[0].index)
  const sections = matches.flatMap((m, index) => {
    const start = m.index + m[0].length
    const end = matches[index + 1]?.index ?? body.length
    const title = m[1]
    const text = body.slice(start, end).trim()
    const h3Matches = [...text.matchAll(/^###\s+(.+)$/gm)]
    const isLevelOnly = !!rankFrom(title) && cleanFeatureTitle(title, '') === ''
    if (!isLevelOnly || !h3Matches.length) return [{ title, text }]
    return h3Matches.map((h3, h3Index) => {
      const h3Start = h3.index + h3[0].length
      const h3End = h3Matches[h3Index + 1]?.index ?? text.length
      return {
        title: h3[1],
        parentRank: rankFrom(title),
        text: text.slice(h3Start, h3End).trim()
      }
    })
  })
  return { intro: stripH1(intro), sections }
}

function featureNameFromSection(section, fallback) {
  const name = cleanFeatureTitle(section.title, fallback)
  if (name !== fallback) return name
  const firstLine = cleanMarkdown(section.text)
    .split('\n')
    .map(line => line.trim())
    .find(line => line && !line.startsWith('|') && !/^[-*]\s+/.test(line))
  if (!firstLine) return name
  const sentenceTitle = firstLine.match(/^(.{3,70}?)\.\s+/)
  if (sentenceTitle) return cleanTitle(sentenceTitle[1])
  if (firstLine.length <= 70) return cleanTitle(firstLine.replace(/[.:]\s*$/, ''))
  return name
}

function rankFrom(text) {
  const match = String(text || '').match(/(?:^|[^\d])(\d{1,2})\s*(?:[-‑–]\s*)?(?:й|м|го|ого|уровень|ур\.|уровня|уровне)/i)
  return match ? Number(match[1]) : null
}

function rankFromOpening(text) {
  const match = String(text || '').slice(0, 700).match(/(?:Начиная\s+с(?:о)?|С|На|Достигнув)\s+(\d{1,2})\s*(?:[-‑–]\s*)?(?:й|м|го|ого|уровня|уровень|уровне|ур\.)/i)
  return match ? Number(match[1]) : null
}

