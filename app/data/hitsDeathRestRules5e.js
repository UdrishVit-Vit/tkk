function sourceName(source) {
  return HITS_DEATH_REST_SOURCE_NAMES_5E[source] || source
}

function makeBlocks(rule) {
  if (rule.blocks?.length) return rule.blocks

  const blocks = [
    {
      title: 'Правило',
      paragraphs: [rule.summary, ...(rule.details || [])]
    }
  ]

  if (rule.bullets?.length) {
    blocks.push({ title: 'Ключевые эффекты', bullets: rule.bullets })
  }

  blocks.push({
    title: 'В игре',
    paragraphs: rule.play
      ? [rule.play]
      : ['Используйте это правило, когда нужно быстро понять, что происходит с существом после урона, лечения, отдыха или потери сознания.']
  })

  return blocks
}

function makeRule(rule) {
  return {
    sourceName: sourceName(rule.source),
    sourceUrl: rule.sourceUrl || HITS_DEATH_REST_SCREEN_5E.sourceUrl,
    quick: rule.quick || [
      { label: 'Источник', value: rule.source },
      { label: 'Раздел', value: rule.groupTitle || 'Хиты, Смерть и Отдых' }
    ],
    terms: rule.terms || [],
    blocks: makeBlocks(rule),
    ...rule
  }
}

