function sourceName(source) {
  return MAGIC_ITEM_SOURCE_NAMES_5E[source] || source
}

function sourceUrl(slug) {
  return `https://5e14.ttg.club/screens/${slug.replaceAll('-', '_')}`
}

function buildBlocks(rule, group) {
  if (group.id === 'random-tables') {
    return [
      {
        title: 'Назначение таблицы',
        paragraphs: [
          rule.summary,
          'Таблицы случайной выборки лучше использовать как инструмент вдохновения. Если результат не подходит сцене, уровню группы или тону кампании, Мастер может выбрать соседний вариант или заменить предмет вручную.'
        ]
      },
      {
        title: 'Как применять',
        paragraphs: [
          'Сначала определите тип сокровища и силу награды, затем выберите нужную таблицу. После броска проверьте, не ломает ли предмет экономику, баланс столкновений или уникальность персонажей.',
          'Случайный предмет станет интереснее, если добавить ему происхождение, владельца, след прошлой истории или небольшое внешнее отличие.'
        ]
      }
    ]
  }

  return [
    {
      title: 'Правило предмета',
      paragraphs: [
        rule.summary,
        'Перед использованием уточните редкость, необходимость настройки, способ активации, число зарядов, расходуемость и то, требуется ли владельцу держать предмет, носить его или произносить командное слово.'
      ]
    },
    {
      title: 'За столом',
      paragraphs: [
        'Магический предмет должен быть понятен игроку: что он делает, когда его можно использовать, что он расходует и какие ограничения у эффекта.',
        'Если предмет проклят, разумен или имеет особое происхождение, лучше заранее определить признаки, по которым персонажи смогут заметить, что вещь не обычная.'
      ]
    }
  ]
}

function makeRule(entry, group) {
  const [slug, title, titleEn, source, icon, summary] = entry

  return {
    slug,
    title,
    titleEn,
    icon,
    source,
    sourceName: sourceName(source),
    sourceUrl: sourceUrl(slug),
    group: group.id,
    groupTitle: group.title || 'Магические предметы',
    summary,
    quick: [
      { label: 'Раздел', value: group.title || 'Магические предметы' },
      { label: 'Источник', value: source },
      { label: 'Тип', value: group.id === 'random-tables' ? 'Таблица' : 'Правило предмета' }
    ],
    terms: [group.title || 'магический предмет', 'редкость', 'настройка', 'заряд', 'активация'],
    blocks: buildBlocks({ summary }, group)
  }
}

export const MAGIC_ITEM_RULE_GROUPS_5E = MAGIC_ITEM_GROUP_DATA_5E.map(group => ({
  id: group.id,
  title: group.title,
  items: group.items.map(item => item[0])
}))

export const MAGIC_ITEM_RULES_5E = MAGIC_ITEM_GROUP_DATA_5E.flatMap(group => group.items.map(item => makeRule(item, group)))

export const MAGIC_ITEM_RULE_BY_SLUG_5E = Object.fromEntries(MAGIC_ITEM_RULES_5E.map(rule => [rule.slug, rule]))

export const MAGIC_ITEM_GROUPS_WITH_RULES_5E = MAGIC_ITEM_RULE_GROUPS_5E.map(group => ({
  ...group,
  rules: group.items.map(slug => MAGIC_ITEM_RULE_BY_SLUG_5E[slug]).filter(Boolean)
}))

