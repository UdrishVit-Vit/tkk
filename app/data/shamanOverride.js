import SHAMAN_DOC from './shamanDoc.generated.json' with { type: 'json' }

const nodeAt = index => SHAMAN_DOC.find(node => node.index === index)
const nodesBetween = (start, end) => SHAMAN_DOC.filter(node => node.index >= start && node.index < end)
const clean = value => String(value || '').replace(/\s+/g, ' ').trim()
const emphasizeParagraphs = (text, headings) => String(text || '')
  .split(/\n{2,}/)
  .map(paragraph => headings.includes(paragraph.trim()) ? `**${paragraph.trim()}**` : paragraph)
  .join('\n\n')

function sourceText(start, end) {
  return nodesBetween(start, end).map(node => {
    if (node.type === 'paragraph') return clean(node.text)
    if (node.type === 'table') {
      const rows = node.rows || []
      if (!rows.length) return ''
      return rows.map((row, index) => `${index ? '|' : '|'} ${row.map(clean).join(' | ')} |`).join('\n')
    }
    return ''
  }).filter(Boolean).join('\n\n')
}

const chantCategory = text => /^(?:Малые Напевы|Напевы (?:Пустоты|Шамаса|Ману|Безумия))$/iu.test(clean(text))
const chantHeading = text => /^Напев\s+/iu.test(clean(text)) && !chantCategory(text)

function buildChants() {
  const nodes = nodesBetween(188, 674).filter(node => node.type === 'paragraph')
  const boundaries = nodes.filter(node => node.bold && (chantCategory(node.text) || chantHeading(node.text)))
  let category = 'Малые напевы'
  const items = []
  boundaries.forEach((heading, index) => {
    if (chantCategory(heading.text)) {
      category = clean(heading.text)
      return
    }
    const next = boundaries.slice(index + 1).find(node => node.index > heading.index)
    const text = sourceText(heading.index + 1, next?.index ?? 674)
    items.push({
      name: clean(heading.text),
      category,
      text
    })
  })
  return items
}

const spiritCategory = text => /^(?:Духи)?Малые духи$|^Средние духи$|^Высшие Духи$|^Древние Духи$/iu.test(clean(text))
function spiritHeading(node) {
  const text = clean(node.text)
  if (!node.bold || text.length > 110 || spiritCategory(text) || /^Напев Духа/iu.test(text)) return false
  return /^Дух\s+/iu.test(text)
    || /\((?:малый|средний|высший|древний) дух/iu.test(text)
    || /[-,]\s*(?:Дух|дух)\b/u.test(text)
}

function buildSpirits() {
  const nodes = nodesBetween(674, 943).filter(node => node.type === 'paragraph')
  const boundaries = nodes.filter(node => spiritCategory(node.text) || spiritHeading(node))
  let category = 'Малые духи'
  const items = []
  boundaries.forEach((heading, index) => {
    if (spiritCategory(heading.text)) {
      category = clean(heading.text).replace(/^Духи/iu, '') || 'Малые духи'
      return
    }
    const next = boundaries.slice(index + 1).find(node => node.index > heading.index)
    const text = sourceText(heading.index + 1, next?.index ?? 943)
    items.push({
      name: clean(heading.text),
      category,
      text
    })
  })
  return items
}

