function makeRule(entry, group) {
  const [slug, title, titleEn, icon, externalSlug, summary] = entry

  return {
    slug,
    title,
    titleEn,
    icon,
    source: 'DMG',
    sourceName: PLANAR_TRAVEL_SCREEN_5E.sourceName,
    sourceUrl: sourceUrl(externalSlug),
    group: group.id,
    groupTitle: group.title,
    summary,
    quick: [
      { label: 'Раздел', value: group.title },
      { label: 'Источник', value: 'DMG' },
      { label: 'Тип', value: ruleType(group) }
    ],
    terms: ['план', 'портал', 'мультивселенная', 'путешествие', 'планарное влияние'],
    blocks: buildBlocks({ summary }, group)
  }
}

