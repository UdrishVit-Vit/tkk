function sourceUrl(externalSlug) {
  return `https://5e14.ttg.club/screens/${externalSlug}`
}

function ruleType(group) {
  if (group.id === 'outer-planes' || group.id === 'inner-planes' || group.id === 'outlands') return 'План'
  if (group.id === 'optional-rules') return 'Опциональное правило'
  if (group.id === 'portals-magic') return 'Переходы и магия'
  if (group.id === 'feywild' || group.id === 'shadowfell' || group.id === 'ethereal-plane') return 'Область плана'
  return 'Планарное правило'
}

