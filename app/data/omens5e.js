// Бросок 4к4: возвращает { dice:[грани 1..4], faces:[имена], sig, omen }.
export function rollOmen() {
  const dice = Array.from({ length: 4 }, () => 1 + Math.floor(Math.random() * 4))
  const sig = [0, 0, 0, 0]
  for (const d of dice) sig[d - 1]++
  return {
    dice,
    faces: dice.map(d => OMEN_FACES[d - 1]),
    sig,
    omen: OMEN_BY_SIG[sig.join(',')] || null
  }
}
