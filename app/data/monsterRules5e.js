function sourceName(source) {
  return MONSTER_SOURCE_NAMES_5E[source] || source
}

function sourceUrl(externalSlug) {
  return `https://5e14.ttg.club/screens/${externalSlug}`
}

function buildBlocks(rule, group) {
  if (group.id === 'monster-types') {
    return [
      {
        title: 'Тип существа',
        paragraphs: [
          rule.summary,
          'Тип монстра помогает быстро понять происхождение существа, его место в мире, возможные знания о нём и взаимодействие с заклинаниями, умениями и магическими предметами.'
        ]
      },
      {
        title: 'Как использовать',
        paragraphs: [
          'При подготовке сцены тип существа помогает выбрать поведение, среду обитания, следы, язык описания и то, какие проверки знаний могут раскрыть важную информацию.',
          'Тип не обязан полностью определять монстра. Его можно сочетать с уникальной культурой, мутацией, проклятием, планарным влиянием или особенностью конкретной кампании.'
        ]
      }
    ]
  }

  return [
    {
      title: 'Правило монстра',
      paragraphs: [
        rule.summary,
        'Эти параметры помогают Мастеру быстро читать статблок, оценивать опасность существа и понимать, как оно будет работать в бою, исследовании или социальной сцене.'
      ]
    },
    {
      title: 'За столом',
      paragraphs: [
        'Используйте правило как практический ориентир: что влияет на сложность столкновения, какую информацию можно дать игрокам и какие числа важны для темпа боя.',
        'Если монстр нужен как центральная угроза, дополните числа поведением, целью, слабостями, окружением и способом, которым персонажи могут узнать о нём до боя.'
      ]
    }
  ]
}

function makeRule(entry, group) {
  const [slug, title, titleEn, source, icon, externalSlug, summary] = entry
  const groupTitle = group.title || 'Монстры'

  return {
    slug,
    title,
    titleEn,
    icon,
    source,
    sourceName: sourceName(source),
    sourceUrl: sourceUrl(externalSlug),
    group: group.id,
    groupTitle,
    summary,
    quick: [
      { label: 'Раздел', value: groupTitle },
      { label: 'Источник', value: source },
      { label: 'Тип', value: group.id === 'monster-types' ? 'Тип монстра' : 'Правило монстра' }
    ],
    terms: group.id === 'monster-types'
      ? ['тип монстра', 'существо', 'статблок', 'происхождение', 'знания']
      : ['монстр', 'показатель опасности', 'опыт', 'хиты', 'размер', 'бонус мастерства'],
    blocks: buildBlocks({ summary }, group)
  }
}

export const MONSTER_RULE_GROUPS_5E = MONSTER_GROUP_DATA_5E.map(group => ({
  id: group.id,
  title: group.title,
  items: group.items.map(item => item[0])
}))

export const MONSTER_RULES_5E = MONSTER_GROUP_DATA_5E.flatMap(group => group.items.map(item => makeRule(item, group)))

export const MONSTER_RULE_BY_SLUG_5E = Object.fromEntries(MONSTER_RULES_5E.map(rule => [rule.slug, rule]))

export const MONSTER_GROUPS_WITH_RULES_5E = MONSTER_RULE_GROUPS_5E.map(group => ({
  ...group,
  rules: group.items.map(slug => MONSTER_RULE_BY_SLUG_5E[slug]).filter(Boolean)
}))

