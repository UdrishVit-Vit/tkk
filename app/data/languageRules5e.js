function sourceUrl(externalSlug) {
  return `https://5e14.ttg.club/screens/${externalSlug}`
}

function buildBlocks(rule, group) {
  if (group.id === 'secret-languages') {
    return [
      {
        title: 'Секретный язык',
        paragraphs: [
          rule.summary,
          'Секретные языки обычно нельзя просто выбрать как обычный разговорный язык без подходящего класса, предыстории, обучения или разрешения Мастера. Их ценность в том, что они скрывают смысл от посторонних.'
        ]
      },
      {
        title: 'За столом',
        paragraphs: [
          'Используйте такой язык для тайных знаков, скрытых сообщений, предупреждений, клятв и общения внутри закрытого сообщества.',
          'Если игрок хочет понять сообщение, сначала определите, знает ли персонаж язык или может ли распознать хотя бы признаки системы записи, жестов или символов.'
        ]
      }
    ]
  }

  return [
    {
      title: group.title,
      paragraphs: [
        rule.summary,
        'Язык помогает связать персонажа с народом, культурой, местом происхождения и кругом общения. Он может открывать переговоры, тексты, легенды, тайные предупреждения и новые способы решения сцены без боя.'
      ]
    },
    {
      title: 'Использование в игре',
      paragraphs: [
        'При выборе языка подумайте, где он будет звучать в кампании: среди союзников, врагов, древних руин, торговцев, планарных существ или тайных обществ.',
        'Мастер может менять список обычных и редких языков под мир кампании, если это лучше отражает географию, историю и народы конкретного сеттинга.'
      ]
    }
  ]
}

function makeRule(entry, group) {
  const [slug, title, titleEn, icon, externalSlug, summary] = entry

  return {
    slug,
    title,
    titleEn,
    icon,
    source: 'PHB',
    sourceName: LANGUAGE_SCREEN_5E.sourceName,
    sourceUrl: sourceUrl(externalSlug),
    group: group.id,
    groupTitle: group.title,
    summary,
    quick: [
      { label: 'Раздел', value: group.title },
      { label: 'Источник', value: 'PHB' },
      { label: 'Тип', value: group.id === 'secret-languages' ? 'Секретный язык' : group.id === 'exotic-languages' ? 'Экзотический язык' : 'Распространённый язык' }
    ],
    terms: ['язык', 'раса', 'предыстория', 'общение', 'культура'],
    blocks: buildBlocks({ summary }, group)
  }
}

export const LANGUAGE_RULE_GROUPS_5E = LANGUAGE_GROUP_DATA_5E.map(group => ({
  id: group.id,
  title: group.title,
  items: group.items.map(item => item[0])
}))

export const LANGUAGE_RULES_5E = LANGUAGE_GROUP_DATA_5E.flatMap(group => group.items.map(item => makeRule(item, group)))

export const LANGUAGE_RULE_BY_SLUG_5E = Object.fromEntries(LANGUAGE_RULES_5E.map(rule => [rule.slug, rule]))

export const LANGUAGE_GROUPS_WITH_RULES_5E = LANGUAGE_RULE_GROUPS_5E.map(group => ({
  ...group,
  rules: group.items.map(slug => LANGUAGE_RULE_BY_SLUG_5E[slug]).filter(Boolean)
}))

export function languageRulePath(slug) {
  return `/dnd5e/screens/language/${slug}`
}
