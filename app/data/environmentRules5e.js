function sourceName(source) {
  return ENVIRONMENT_SOURCE_NAMES_5E[source] || source
}

function makeBlocks(rule) {
  if (rule.blocks?.length) return rule.blocks

  const blocks = [
    {
      title: 'Правило',
      paragraphs: [rule.summary, ...(rule.details || [])]
    }
  ]

  if (rule.bullets?.length) {
    blocks.push({ title: 'Ключевые эффекты', bullets: rule.bullets })
  }

  blocks.push({
    title: 'В игре',
    paragraphs: rule.play
      ? [rule.play]
      : ['Используйте это правило, когда окружение реально влияет на решения персонажей: ограничивает видимость, меняет движение, создаёт риск урона, требует проверки или заставляет искать обходной путь.']
  })

  return blocks
}

function makeRule(rule) {
  return {
    sourceName: sourceName(rule.source),
    sourceUrl: rule.sourceUrl || ENVIRONMENT_SCREEN_5E.sourceUrl,
    quick: rule.quick || [
      { label: 'Источник', value: rule.source },
      { label: 'Раздел', value: rule.groupTitle || 'Окружающая среда' }
    ],
    terms: rule.terms || [],
    blocks: makeBlocks(rule),
    ...rule
  }
}

